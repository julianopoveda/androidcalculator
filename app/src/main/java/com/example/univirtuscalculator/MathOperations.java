package com.example.univirtuscalculator;

public class MathOperations {
    public float Plus(float operand, float operator) {
        return operand + operator;
    }

    public float Minus(float operand, float operator) {
        return operand - operator;
    }

    public float Multiplication(float operand, float operator) {
        return operand * operator;
    }

    public float Division(float operand, float operator) {
        return operand / operator;
    }
}
